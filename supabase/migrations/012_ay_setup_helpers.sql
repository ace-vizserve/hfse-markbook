-- 012_ay_setup_helpers.sql
--
-- AY Setup Wizard — Postgres helper functions. See docs/context/18-ay-setup.md
-- for the full design. These functions are the atomicity layer: Supabase JS
-- can't run multi-statement transactions from the client, so the compound
-- operations (create a whole AY, delete a whole AY) live here.
--
-- Security: all functions are `security definer` so they can run DDL. Execute
-- is REVOKEd from public and GRANTed only to `service_role` — the app calls
-- them exclusively via `createServiceClient()` in the API route.
--
-- Template source of truth: docs/context/10-parent-portal.md §"Reference DDL".
-- When the parent portal bumps the admissions schema, update BOTH the doc AND
-- the `create_ay_admissions_tables` function body below. lib/sis/ay-setup/
-- admissions-ddl.ts carries a TypeScript mirror for reference.
--
-- See docs/context/18-ay-setup.md §"Admissions DDL" for the coordination rule.

