-- =====================================================================
-- drop_ay_admissions_tables(p_ay_slug text)
-- =====================================================================
-- Drops the 4 AY-prefixed admissions tables. Pre-flight guards verify
-- every target has zero rows; raises if any are populated. Idempotent
-- via DROP TABLE IF EXISTS.

create or replace function public.drop_ay_admissions_tables(p_ay_slug text)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  v_slug       text := lower(trim(p_ay_slug));
  v_table      text;
  v_rowcount   bigint;
  v_tables     text[] := array[
    'enrolment_applications',
    'enrolment_status',
    'enrolment_documents',
    'discount_codes'
  ];
begin
  if v_slug !~ '^ay[0-9]{2}$' then
    raise exception 'Invalid AY slug: %. Expected format like "ay27".', p_ay_slug;
  end if;

  foreach v_table in array v_tables loop
    if to_regclass(format('public.%I', v_slug || '_' || v_table)) is not null then
      execute format('select count(*) from public.%I', v_slug || '_' || v_table)
        into v_rowcount;
      if v_rowcount > 0 then
        raise exception 'Cannot drop %_%: table has % rows. Refusing to destroy data.',
          v_slug, v_table, v_rowcount;
      end if;
    end if;
  end loop;

  execute format('drop table if exists public.%I', v_slug || '_enrolment_documents');
  execute format('drop table if exists public.%I', v_slug || '_enrolment_status');
  execute format('drop table if exists public.%I', v_slug || '_enrolment_applications');
  execute format('drop table if exists public.%I', v_slug || '_discount_codes');
end;
$$;

revoke all on function public.drop_ay_admissions_tables(text) from public;
grant execute on function public.drop_ay_admissions_tables(text) to service_role;

-- =====================================================================
-- create_academic_year(p_ay_code text, p_label text)
-- =====================================================================
-- Atomically creates everything a new AY needs to function inside the SIS:
--   1. Inserts a row into academic_years (is_current=false).
--   2. Inserts 4 terms (T1–T4) with NULL dates.
--   3. Copies sections from the most-recent prior AY (by ay_code desc).
--   4. Copies subject_configs from the most-recent prior AY.
--   5. Creates the 4 AY-prefixed admissions tables (via helper above).
--
-- Returns JSON: { ay_id, sections_copied, subject_configs_copied,
--                 terms_created, tables_created }.
-- Raises on duplicate ay_code or invalid format.

create or replace function public.create_academic_year(
  p_ay_code text,
  p_label   text
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_code             text := upper(trim(p_ay_code));
  v_label            text := trim(p_label);
  v_slug             text;
  v_ay_id            uuid;
  v_source_ay_id     uuid;
  v_sections_copied  int;
  v_configs_copied   int;
begin
  if v_code !~ '^AY[0-9]{4}$' then
    raise exception 'Invalid AY code: %. Expected format AY2027.', p_ay_code;
  end if;
  if v_label is null or v_label = '' then
    raise exception 'AY label is required.';
  end if;

  v_slug := 'ay' || substring(v_code from 5 for 2);

  -- 1. academic_years row
  insert into public.academic_years (ay_code, label, is_current)
  values (v_code, v_label, false)
  returning id into v_ay_id;

  -- 2. terms (T1–T4, nullable dates)
  insert into public.terms (academic_year_id, term_number, label, is_current)
  select v_ay_id, n, 'Term ' || n || ' — ' || v_code, false
  from generate_series(1, 4) as g(n);

  -- 3. Pick source AY for copy-forward: most recent by ay_code desc,
  --    excluding the one we just inserted.
  select id into v_source_ay_id
  from public.academic_years
  where id <> v_ay_id
  order by ay_code desc
  limit 1;

  if v_source_ay_id is not null then
    -- Copy sections
    insert into public.sections (academic_year_id, level_id, name, class_type, form_class_adviser)
    select v_ay_id, level_id, name, class_type, null
    from public.sections
    where academic_year_id = v_source_ay_id;

    get diagnostics v_sections_copied = row_count;

    -- Copy subject_configs
    insert into public.subject_configs (
      academic_year_id, subject_id, level_id,
      ww_weight, pt_weight, qa_weight, ww_max_slots, pt_max_slots
    )
    select v_ay_id, subject_id, level_id,
           ww_weight, pt_weight, qa_weight, ww_max_slots, pt_max_slots
    from public.subject_configs
    where academic_year_id = v_source_ay_id;

    get diagnostics v_configs_copied = row_count;
  else
    v_sections_copied := 0;
    v_configs_copied  := 0;
  end if;

  -- 4. Admissions DDL
  perform public.create_ay_admissions_tables(v_slug);

  return jsonb_build_object(
    'ay_id', v_ay_id,
    'ay_code', v_code,
    'ay_slug', v_slug,
    'terms_created', 4,
    'sections_copied', v_sections_copied,
    'subject_configs_copied', v_configs_copied,
    'tables_created', jsonb_build_array(
      v_slug || '_enrolment_applications',
      v_slug || '_enrolment_status',
      v_slug || '_enrolment_documents',
      v_slug || '_discount_codes'
    )
  );
end;
$$;

revoke all on function public.create_academic_year(text, text) from public;
grant execute on function public.create_academic_year(text, text) to service_role;

-- =====================================================================
-- delete_academic_year(p_ay_code text)
-- =====================================================================
-- Atomically deletes an AY if and only if it has no data anywhere.
-- Guards:
--   - Not is_current.
--   - No section_students for any section in this AY.
--   - No grading_sheets for any term in this AY.
--   - No admissions rows in any of the 4 AY-prefixed tables.
--   - No p_file_revisions for this AY.
--   - No attendance_records / report_card_comments / report_card_publications /
--     grade_change_requests via the AY's sheets/terms.
--
-- If any blocker is present, raises with a descriptive message. Otherwise
-- runs the drop_ay_admissions_tables helper and deletes the SIS rows.

create or replace function public.delete_academic_year(p_ay_code text)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_code        text := upper(trim(p_ay_code));
  v_slug        text;
  v_ay_id       uuid;
  v_is_current  boolean;
  v_count       bigint;
  v_blocker     text;
  v_configs     int;
  v_sections    int;
  v_terms       int;
  v_total_rows  bigint;
begin
  if v_code !~ '^AY[0-9]{4}$' then
    raise exception 'Invalid AY code: %. Expected format AY2027.', p_ay_code;
  end if;

  v_slug := 'ay' || substring(v_code from 5 for 2);

  select id, is_current
    into v_ay_id, v_is_current
  from public.academic_years
  where ay_code = v_code;

  if v_ay_id is null then
    raise exception 'AY % not found.', v_code;
  end if;

  if v_is_current then
    raise exception 'Cannot delete % — it is the current AY. Switch active first.', v_code;
  end if;

  -- Guard: section_students
  select count(*) into v_count
  from public.section_students ss
  join public.sections s on s.id = ss.section_id
  where s.academic_year_id = v_ay_id;
  if v_count > 0 then
    v_blocker := v_count || ' section_students rows';
    raise exception 'Cannot delete %: %', v_code, v_blocker;
  end if;

  -- Guard: grading_sheets (via terms or sections in this AY)
  select count(*) into v_count
  from public.grading_sheets gs
  where gs.term_id in (select id from public.terms where academic_year_id = v_ay_id)
     or gs.section_id in (select id from public.sections where academic_year_id = v_ay_id);
  if v_count > 0 then
    v_blocker := v_count || ' grading_sheets rows';
    raise exception 'Cannot delete %: %', v_code, v_blocker;
  end if;

  -- Guard: attendance_records (via terms in this AY)
  select count(*) into v_count
  from public.attendance_records
  where term_id in (select id from public.terms where academic_year_id = v_ay_id);
  if v_count > 0 then
    v_blocker := v_count || ' attendance_records rows';
    raise exception 'Cannot delete %: %', v_code, v_blocker;
  end if;

  -- Guard: report_card_publications (via terms in this AY)
  select count(*) into v_count
  from public.report_card_publications
  where term_id in (select id from public.terms where academic_year_id = v_ay_id);
  if v_count > 0 then
    v_blocker := v_count || ' report_card_publications rows';
    raise exception 'Cannot delete %: %', v_code, v_blocker;
  end if;

  -- Guard: p_file_revisions matching this AY code
  select count(*) into v_count
  from public.p_file_revisions
  where ay_code = v_code;
  if v_count > 0 then
    v_blocker := v_count || ' p_file_revisions rows';
    raise exception 'Cannot delete %: %', v_code, v_blocker;
  end if;

  -- Guard: admissions tables populated
  v_total_rows := 0;
  if to_regclass(format('public.%I', v_slug || '_enrolment_applications')) is not null then
    execute format('select count(*) from public.%I', v_slug || '_enrolment_applications')
      into v_count;
    v_total_rows := v_total_rows + v_count;
  end if;
  if to_regclass(format('public.%I', v_slug || '_enrolment_status')) is not null then
    execute format('select count(*) from public.%I', v_slug || '_enrolment_status')
      into v_count;
    v_total_rows := v_total_rows + v_count;
  end if;
  if to_regclass(format('public.%I', v_slug || '_enrolment_documents')) is not null then
    execute format('select count(*) from public.%I', v_slug || '_enrolment_documents')
      into v_count;
    v_total_rows := v_total_rows + v_count;
  end if;
  if to_regclass(format('public.%I', v_slug || '_discount_codes')) is not null then
    execute format('select count(*) from public.%I', v_slug || '_discount_codes')
      into v_count;
    v_total_rows := v_total_rows + v_count;
  end if;
  if v_total_rows > 0 then
    raise exception 'Cannot delete %: admissions tables hold % rows total.', v_code, v_total_rows;
  end if;

  -- All guards passed — proceed with delete
  perform public.drop_ay_admissions_tables(v_slug);

  delete from public.subject_configs where academic_year_id = v_ay_id;
  get diagnostics v_configs = row_count;

  delete from public.sections where academic_year_id = v_ay_id;
  get diagnostics v_sections = row_count;

  delete from public.terms where academic_year_id = v_ay_id;
  get diagnostics v_terms = row_count;

  delete from public.academic_years where id = v_ay_id;

  return jsonb_build_object(
    'ay_code', v_code,
    'ay_slug', v_slug,
    'subject_configs_deleted', v_configs,
    'sections_deleted', v_sections,
    'terms_deleted', v_terms,
    'tables_dropped', jsonb_build_array(
      v_slug || '_enrolment_applications',
      v_slug || '_enrolment_status',
      v_slug || '_enrolment_documents',
      v_slug || '_discount_codes'
    )
  );
end;
$$;

