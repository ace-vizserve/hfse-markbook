// Default fill for a required slot that a persona didn't explicitly declare.
// 'To follow' = parent acknowledged but hasn't uploaded yet — the only valid
// non-null state when we don't have a real document URL to attach.
const TO_FOLLOW: DocFill = { status: 'To follow', url: null };

function buildDocsRow(p: Persona): Record<string, unknown> {
  // Merge persona docs with completeness defaults so the DB row never carries
  // a null status on a required slot (plan invariant; verified by the post-seed
  // SQL checks in the plan). The order of precedence: persona explicit > default.
  const docs: Record<string, DocFill> = { ...p.docs };

  // Student document slots — always required (KD #60).
  for (const slot of [
    'idPicture',
    'birthCert',
    'educCert',
    'medical',
    'form12',
    'passport',
    'pass',
  ]) {
    if (!docs[slot]) docs[slot] = TO_FOLLOW;
  }

  // Mother slots — always required (anchor parent, always present per KD #69).
  for (const slot of ['motherPassport', 'motherPass']) {
    if (!docs[slot]) docs[slot] = TO_FOLLOW;
  }

  // Father slots — required only when fatherEmail is set (KD #69 gate).
  if (p.father?.email) {
    for (const slot of ['fatherPassport', 'fatherPass']) {
      if (!docs[slot]) docs[slot] = TO_FOLLOW;
    }
  }

  // Guardian slots — required only when guardianEmail is set (KD #69 gate).
  if (p.guardian?.email) {
    for (const slot of ['guardianPassport', 'guardianPass']) {
      if (!docs[slot]) docs[slot] = TO_FOLLOW;
    }
  }

  const row: Record<string, unknown> = {
    enroleeNumber: p.enroleeNumber,
    studentNumber: p.studentNumber,
    idPictureUploadedDate:
      docs['idPicture']?.status === 'Uploaded' ||
      docs['idPicture']?.status === 'Valid'
        ? isoDateOffset(p.enrolmentDateOffset)
        : null,
    uploadFormDocument: null,
  };
  for (const [key, fill] of Object.entries(docs)) {
    row[key] = fill.url;
    row[`${key}Status`] = fill.status;
    if (fill.expiryDays != null) {
      row[`${key}Expiry`] = isoDateOffset(fill.expiryDays);
    }
  }
  return row;
}
